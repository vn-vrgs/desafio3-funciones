let a = 22;
let b = 33;

// Arrow function de una sola línea (con return implícito)
let suma = (a, b) => a + b;

// Invocando a la función
let retorno = suma(a, b);
// Enviando por consola
console.log("El resultado es: " + retorno); // 55
// Enviando por navegador
alert("El resultado es: " + retorno); // 55

